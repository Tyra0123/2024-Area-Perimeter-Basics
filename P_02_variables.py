# Code to learn about variables...

# Variables can be a string (note the speech marks)
greeting = "hello world"

# Variables can be a number
fav_num = 1
num_string = "5"

# Variables can be Boolean (True / False)
love_programming = True

print(greeting)
print(fav_num)
print(love_programming)
print(fav_num * 2)
print(num_string * 5)

